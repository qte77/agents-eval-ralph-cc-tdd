"""Core data models for papers and reviews.

Defines Pydantic models for Paper and Review entities from PeerRead dataset.
"""

from __future__ import annotations

from pydantic import BaseModel, Field


class Paper(BaseModel):
    """Paper model representing a scientific paper."""

    paper_id: str = Field(..., description="Unique paper identifier")
    title: str = Field(..., description="Paper title")
    abstract: str = Field(..., description="Paper abstract")
    content: str = Field(..., description="Full paper content")
    metadata: dict[str, str | int] = Field(
        default_factory=dict, description="Additional paper metadata"
    )


class Review(BaseModel):
    """Review model representing a paper review."""

    review_id: str = Field(..., description="Unique review identifier")
    paper_id: str = Field(..., description="ID of paper being reviewed")
    rating: int = Field(..., description="Numerical rating of the paper")
    confidence: int = Field(..., description="Reviewer confidence level")
    summary: str = Field(..., description="Review summary")
    strengths: list[str] = Field(..., description="Paper strengths")
    weaknesses: list[str] = Field(..., description="Paper weaknesses")
    detailed_comments: str = Field(..., description="Detailed review comments")
    is_agent_generated: bool = Field(..., description="Whether review was generated by agent")
